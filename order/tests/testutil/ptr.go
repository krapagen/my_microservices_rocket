package testutil

// Ptr возвращает указатель на переданное значение.
//
// Используется в табличных тестах, где нужен указатель на литерал
// (например, ShieldUUID *string в CreateOrderRequest). Встроенный new(T)
// (Go 1.25+) подходит для нулевых значений; Ptr нужен именно для случаев,
// когда требуется указатель на конкретный литерал
func Ptr[T any](v T) *T { return &v }
